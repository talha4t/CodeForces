#include <bits/stdc++.h>
using namespace std;

#define ed '\n'

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while(t--) {
        int n;
        cin >> n;

        int x;
        for (int i = 0; i < n; i++)
            cin >> x;

        cout << x << ed;
    }
    
    return 0;
}

